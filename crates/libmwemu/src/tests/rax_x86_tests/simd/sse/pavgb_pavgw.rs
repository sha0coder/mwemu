use crate::*;

// PAVGB/PAVGW - Average Packed Integers (SSE/SSE2)
//
// Performs SIMD average of packed unsigned integers from source and destination operands.
// Stores packed integer results in destination. Average computed as (a + b + 1) >> 1 with rounding.
//
// PAVGB: Average 16 packed unsigned byte integers (8-bit each)
// PAVGW: Average 8 packed unsigned word integers (16-bit each)
//
// Formula: result = (src1 + src2 + 1) >> 1
//
// Opcodes (SSE2 - 128-bit XMM):
// 66 0F E0 /r      PAVGB xmm1, xmm2/m128   - Average packed unsigned bytes
// 66 0F E3 /r      PAVGW xmm1, xmm2/m128   - Average packed unsigned words

const ALIGNED_ADDR: u64 = 0x3000;
const ALIGNED_ADDR2: u64 = 0x3100;

// ============================================================================
// PAVGB Tests - 16x Unsigned Byte Averaging
// ============================================================================

#[test]
fn test_pavgb_all_zeros() {
    let mut emu = emu64();
    let code = [0x48, 0xb8];
    let mut full_code = code.to_vec();
    full_code.extend_from_slice(&ALIGNED_ADDR.to_le_bytes());
    full_code.extend_from_slice(&[0x48, 0xbb]);
    full_code.extend_from_slice(&ALIGNED_ADDR2.to_le_bytes());
    full_code.extend_from_slice(&[
        0x66, 0x0f, 0x6f, 0x00, // MOVDQA XMM0, [RAX]
        0x66, 0x0f, 0x6f, 0x0b, // MOVDQA XMM1, [RBX]
        0x66, 0x0f, 0xe0, 0xc1, // PAVGB XMM0, XMM1
        0xf4, // HLT
    ]);

    emu.load_code_bytes(&full_code);
    emu.maps.write_bytes_slice(ALIGNED_ADDR, &[0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00]);
    emu.maps.write_bytes_slice(ALIGNED_ADDR2, &[0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00]);
    emu.run(None).unwrap();
}

