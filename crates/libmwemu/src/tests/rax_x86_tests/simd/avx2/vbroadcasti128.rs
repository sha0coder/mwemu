use crate::*;

// VBROADCASTI128 - Broadcast 128-bit Integer Data (AVX2)
//
// Broadcasts a 128-bit value from memory to both 128-bit lanes of a YMM register.
// The lower 128 bits and upper 128 bits of the destination will both contain
// the same 128-bit value from memory.
//
// Opcodes (AVX2 - 256-bit YMM):
// VEX.256.66.0F38.W0 5A /r       VBROADCASTI128 ymm1, m128

const ALIGNED_ADDR: u64 = 0x3000;

// ============================================================================
// VBROADCASTI128 Tests - Broadcast 128-bit to Both Lanes (256-bit)
// ============================================================================

#[test]
fn test_vbroadcasti128_ymm0_mem() {
    let mut emu = emu64();
    // VBROADCASTI128 YMM0, [memory]
    let code = [0x48, 0xb8];
    let mut full_code = code.to_vec();
    full_code.extend_from_slice(&ALIGNED_ADDR.to_le_bytes());
    full_code.extend_from_slice(&[
        0xc4, 0xe2, 0x7d, 0x5a, 0x00, // VBROADCASTI128 YMM0, [RAX]
        0xf4, // HLT
    ]);

    emu.load_code_bytes(&full_code);
    let data = vec![
        0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08,
        0x09, 0x0A, 0x0B, 0x0C, 0x0D, 0x0E, 0x0F, 0x10,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    ];
    emu.maps.write_bytes_slice(ALIGNED_ADDR, &data);
    emu.run(None).unwrap();
}

