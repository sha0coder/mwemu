// VPGATHERQD Tests - Gather 4 Dwords Using Qword Indices (256-bit indices, 128-bit result)
// ============================================================================

#[test]
fn test_vpgatherqd_xmm0_base_ymm2_xmm1() {
    let mut emu = emu64();
    // VPGATHERQD XMM0, [RAX + YMM2*1], XMM1
    let code = [0x48, 0xb8];
    let mut full_code = code.to_vec();
    full_code.extend_from_slice(&GATHER_DATA_ADDR.to_le_bytes());
    full_code.extend_from_slice(&[
        0xc4, 0xe2, 0x75, 0x91, 0x04, 0x10, // VPGATHERQD XMM0, [RAX + YMM2*1], XMM1
        0xf4, // HLT
    ]);

    emu.load_code_bytes(&full_code);
    let mut data = vec![0u8; 256];
    for i in 0..64 {
        data[i * 4] = (i & 0xFF) as u8;
        data[i * 4 + 1] = ((i >> 8) & 0xFF) as u8;
        data[i * 4 + 2] = 0;
        data[i * 4 + 3] = 0;
    }
    emu.maps.write_bytes_slice(GATHER_DATA_ADDR, &data);
    emu.run(None).unwrap();
}

