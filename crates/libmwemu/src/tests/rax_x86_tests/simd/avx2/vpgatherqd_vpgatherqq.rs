use crate::*;

// VPGATHERQD/VPGATHERQQ - Gather Packed Dword/Qword Values Using Qword Indices (AVX2)
//
// Gathers data from memory at locations specified by base address + qword indices.
// Uses a mask register to control which elements are loaded.
// The mask is also used to prevent page faults on unused elements.
//
// VPGATHERQD: Gather 4 packed dwords (32-bit) using 4 qword indices, result in XMM (lower 128)
// VPGATHERQQ: Gather 4 packed qwords (64-bit) using 4 qword indices
//
// Opcodes (AVX2 - 256-bit YMM):
// VEX.256.66.0F38.W0 91 /r       VPGATHERQD xmm1, vm64y, xmm2
// VEX.256.66.0F38.W1 91 /r       VPGATHERQQ ymm1, vm64y, ymm2

const ALIGNED_ADDR: u64 = 0x3000;
const GATHER_DATA_ADDR: u64 = 0x4000;

